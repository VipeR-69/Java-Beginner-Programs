public class expression{
    public static void main(String[] args){

        // expression = operands & operators
        // operands = values, variables, number, quantity
        // operators  - + - * / %

        int friends = 10;

        friends--;
        System.out.println(friends);

        friends = friends + 1;
        System.out.println(friends);

        friends = friends / 2;
        System.out.println(friends);

        friends = friends * 2;
        System.out.println(friends);

        friends = friends % 3;
        System.out.println(friends);

        friends++;
        System.out.println(friends);

    }
}